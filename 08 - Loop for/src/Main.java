/* ~ Loop for ~

    Loop for adalah tipe lain dari loop.
    Di dalam (), kita menginisialisasi variable, menambahkan kondisi, dan memperbarui variable.
    Setiap langkah dipisahkan oleh titik koma ; tetapi pastikan bahwa tidak ada titik koma setelah variable terakhir.
    Anda dapat membandingkan dua jenis loop pada gambar di bawah.
 */
public class Main {
    public static void main(String[] args) throws Exception {
        
            //   1         2      3
        for (int i = 0; i <= 10; i++){
            System.out.println(i); 
        }   //                 4
    }
}
/* ~ Loop for ~
    1. Inisialisasi variabel 
    2. Memeriksa kondisi 
    3. Iterasi code 
    4. Perbaharui variabel

*/