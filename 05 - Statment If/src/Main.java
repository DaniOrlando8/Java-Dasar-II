/* ~ Statement IF ~ 

    Statement if adalah jenis control flow yang paling umum. 
    Dengan meletakkan kondisi didalam () setelah if, code di dalam {} hanya dijalankan saat kondisinya true.
    Ini penting!

    Mari kita lihat contoh statement if. 
    Boolean, operator perbandingan (==, <, dll) dan operator logika (&&, ||, !) 
    Akan sering digunakan dalam kondisi statement if.
*/
public class Main {
    public static void main(String[] args) {
        int x = 10;
        if (x == 10){
            System.out.println("Nilai x = true, x sama dengan 10");
        }
    }
}
