/* ~ Enhanced Loop for ~

    Enhanced loop for adalah tipe khusus dari loop for yang digunakan dengan array.
    Ini adalah cara yang lebih sederhana untuk menulis loop for.
    Lihat contoh di bawah untuk sintaks dari enhanced loop for.
*/
public class Main {
    public static void main(String[] args) {
        
        String [] nama ={"DTS", "DICODING", "PROGATE", "DQLab"};
        for (String belajar: nama){
                System.out.println(belajar);
        } //enhanced loop for untuk membuat code Anda lebih sederhana!
    }
}
